// 1. Arithmetic Operators
// 2. Unary Operators
// 3. Assignment Operators
// 4. Comparison Operators


// Arithmetic Operators
let a = 10;
let b = 12;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);


// Unary Operators
let x = 5;
console.log(x++); // 5
console.log(++x); // 7


// Assignment Operator
let p = 10;
let q = 11;

q = p;
console.log(q);


// Comparison Operator
let m = 5;
let n = 6;

console.log(m <= 18);