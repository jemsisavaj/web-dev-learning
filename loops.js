// Topic: Loops
// Concepts Covered:
// 1. for loop
// 2. while loop
// 3. nested loops
// 4. loops with arrays

// for loop
for (let i = 1; i <= 5; i++) {
    console.log(i);
}


// reverse loop
for (let i = 10; i > 0; i--) {
    console.log(i);
}


// odd numbers
for (let i = 1; i <= 10; i += 2) {
    console.log(i);
}


// while loop
let i = 1;

while (i <= 5) {
    console.log(i);
    i++;
}